// ─── Main Analysis Function ───────────────────────────────────────────────────

export async function analyseDocument(text: string): Promise<AnalysisResult> {
  if (!text || text.trim().length === 0) {
    throw new Error('Cannot analyse empty text');
  }

  // Run grammar check and AI scoring in parallel
  const [grammarIssues, aiLikelihoodScore, suggestions] = await Promise.all([
    checkGrammar(text),
    scoreAILikelihood(text),
    generateSuggestions(text),
  ]) as [GrammarIssue[], number | null, AISuggestion[]];

  const { score: readabilityScore, wordCount, sentenceCount } =
    computeReadabilityScore(text);

  const plagiarismScore = computePlagiarismScore();
  const grammarScore = computeGrammarScore(wordCount, grammarIssues);

  return {
    aiLikelihoodScore,
    grammarIssues,
    grammarScore,
    plagiarismScore,
    suggestions,
    readabilityScore,
    wordCount,
    sentenceCount,
    analyzedAt: new Date(),
  };
}
