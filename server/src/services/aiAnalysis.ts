// ─── Main Analysis Function ───────────────────────────────────────────────────

export async function analyseDocument(text: string): Promise<AnalysisResult> {
  if (!text || text.trim().length < MIN_ANALYSIS_CHARS) {
    throw new Error(`Text is too short to analyse (minimum ${MIN_ANALYSIS_CHARS} characters).`);
  }

  // Grammar runs on more text (LanguageTool is cheap); Gemini calls are capped via their own slicing
  const grammarText = text.slice(0, MAX_GRAMMAR_CHARS);

  const [grammarIssues, aiContent, suggestions, readabilityData, toneAnalysis] =
    await Promise.all([
      checkGrammar(grammarText),
      analyseAIContent(text),
      generateSuggestions(text),
      computeReadabilityScore(text),
      computeToneAnalysis(text),
    ]) as [GrammarIssue[], Awaited<ReturnType<typeof analyseAIContent>>, AISuggestion[], Awaited<ReturnType<typeof computeReadabilityScore>>, ToneAnalysis];

  const { score: readabilityScore, wordCount, sentenceCount, readingTimeMinutes, fleschGradeLevel, avgSentenceLength } = readabilityData;
  const plagiarismScore = computePlagiarismScore();
  const grammarScore = computeGrammarScore(wordCount, grammarIssues);

  return {
    aiLikelihoodScore: aiContent.score,
    aiReasoning: aiContent.reasoning,
    humanizationTips: aiContent.humanizationTips,
    grammarIssues,
    grammarScore,
    plagiarismScore,
    suggestions,
    readabilityScore,
    wordCount,
    sentenceCount,
    readingTimeMinutes,
    fleschGradeLevel,
    avgSentenceLength,
    toneAnalysis,
    analyzedAt: new Date(),
  };
}
